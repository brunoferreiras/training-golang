package main

import "fmt"

func main() {
	var initial = "initial"
	fmt.Println(initial) // initial

	var numberOne, numberTwo int = 1, 2
	fmt.Println(numberOne, numberTwo) // 1 2

	var boolean = true
	fmt.Println(boolean) // true

	var integer int
	fmt.Println(integer) // 0

	fruit := "apple"
	fmt.Println(fruit) // apple
}